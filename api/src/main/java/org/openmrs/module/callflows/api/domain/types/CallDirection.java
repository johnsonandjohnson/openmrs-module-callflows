package org.openmrs.module.callflows.api.domain.types;

/**
 * A direction of a call with respect to the system
 *
 * @author bramak09
 */
public enum CallDirection {

    /**
     * An outgoing call, i.e. initiated by the system
     */
    OUTGOING,

    /**
     * An incoming call, i.e. received by the system
     */
    INCOMING
}
